export type Layer = 'parse' | 'structural' | 'cross-ref' | 'derive' | 'internal';

export type QsmError = {
  layer: Layer;
  code: QsmErrorCode;
  message: string;
  path?: string;
  hint?: string;
};

export type Ok<T> = { ok: true; value: T };
export type Err = { ok: false; errors: QsmError[] };
export type Result<T> = Ok<T> | Err;

export const ok = <T>(value: T): Ok<T> => ({ ok: true, value });
export const err = (errors: QsmError[]): Err => ({ ok: false, errors });
export const isOk = <T>(r: Result<T>): r is Ok<T> => r.ok;
export const isErr = <T>(r: Result<T>): r is Err => !r.ok;

